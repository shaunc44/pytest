def add(a, b):
    '''
    >>> add(5, 3)
    8
    >>> add('he', 'llo')
    'hello'
    '''
    return a + b


def subtract(a, b):
    '''
    >>> subtract(20, 16)
    4
    >>> subtract(6.5, 4.3)
    2.2
    '''
    return a - b


def multiply(a, b):
    '''
    >>> mulitiply(11, 8)
    88
    >>> mulitiply('yo', 2)
    'yoyo'
    '''
    return a * b


def divide(a, b):
    '''
    >>> divide(100, 10)
    10.0
    >>> divide(0.5, 0.1)
    5.0
    '''
    return a / b
